package com.example.dashboard;

public class Review {
    private String name;
    private String review;

    public Review(String name, String review) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getReview() {
        return review;
    }

    public void setReview(String review) {
        this.review = review;
    }
}